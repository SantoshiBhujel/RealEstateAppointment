    public function slotSearch(Request $request)
    {
        $days= WorkingHour::where('date',$request->date)->get();

        if(count($days)==0)
        {
            return back()->with('error',"Office's day off.");
        }

        $date=$request->date;
        foreach($days as $day)
        {
            $slots=$day->timeslots->where('available', "yes");
        }
        
        return view('home',compact('slots','date'));
